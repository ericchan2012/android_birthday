package com.ds.utility;

public class Person {

	String name;
	String birthDay;
	int gender;
	int isLunar;
	int ringtype;
	String ringDays;
	

	public int getRingtype() {
		return ringtype;
	}

	public void setRingtype(int ringtype) {
		this.ringtype = ringtype;
	}

	public String getRingDays() {
		return ringDays;
	}

	public void setRingDays(String ringDays) {
		this.ringDays = ringDays;
	}

	public int getIsLunar() {
		return isLunar;
	}

	public void setIsLunar(int isLunar) {
		this.isLunar = isLunar;
	}

	public int getGender() {
		return gender;
	}

	public void setGender(int gender) {
		this.gender = gender;
	}

	public String getBirthDay() {
		return birthDay;
	}

	public void setBirthDay(String birthDay) {
		this.birthDay = birthDay;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
